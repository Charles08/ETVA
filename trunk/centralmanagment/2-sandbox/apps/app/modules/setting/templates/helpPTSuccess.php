<a id="help-settings-main"><h1>Preferências</h1></a>
<p>O menu de preferências é apresentado em dois separadores, que possibilitam a definição de diversas opções de configuração do <em>Central Management</em> e de servidores.
<p>Os separadores referidos são:</p>
<ul>
    <li><a href="#help-settings-general">Geral</a></li>
    <li><a href="#help-settings-conn">Conectividade</a></li>
</ul>
<br/>
<hr/>


<a id="help-settings-general"><h1>Geral</h1></a>

<!-- <p>Acedendo a Preferências é possível definir alguns parâmetros globais ao sistema.</p> -->
<p>No separador de opções gerais encontram-se os seguintes parâmetros:</p>

<p><b>Opções VNC - </b>Permite especificar o <em>keymap</em> usado por omissão no acesso por VNC às máquinas
virtuais.</p>
<p><b>Opções de Registo de Eventos - </b>possibilita definir a duração dos registos de eventos do sistema.</p>
<a href="#help-settings-main"><div>Início</div></a>
<hr/>

<a id="help-settings-conn"><h1>Conectividade</h1></a>
<p>No painel Conectividade é permitido alterar o IP do <em>Central Management</em> e da rede LAN.</p>
<p><b>Nota: </b>na versão <em>ETVA Enterprise</em> apenas é possível configurar o IP do CM.</p>
