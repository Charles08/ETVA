<a id="help-settings-main"><h1>Settings</h1></a>
<p>The preferences menu is displayed in two tabs, which allow the definition of the various central management configuration options and servers' configuration.</p>
<p> The tabs above are:</p>
<ul>
    <li><a href="#help-settings-general">General</a></li>
    <li><a href="#help-settings-conn">Connectivity</a></li>
</ul>
<br/>
<hr/>


<a id="help-settings-general"><h1>General</h1></a>

<!-- <p>Acedendo a Preferências é possível definir alguns parâmetros globais ao sistema.</p> -->
<p>In the General settings tab we can configure the following parameters:</p>

<p><b>VNC options - </b>
Lets you specify the keymap used by default for VNC access to virtual servers.
</p>
<p><b>Event log options - </b>
lets you set the duration of the event logs records.
</p>
