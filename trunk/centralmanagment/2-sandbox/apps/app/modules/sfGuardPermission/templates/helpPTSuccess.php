<a id="help-sfguard-permission-main"><h1>Gestão de permissões</h1></a>
<p>Neste separador encontram-se as opções de gestão de permissões. A listagem (em baixo sobre a esquerda) indica os
permissões existentes no sistema. As linhas da listagem podem ser seleccionadas sempre que se pretender efectuar alterações.</p>
<p>Estão disponíveis as seguintes operações:</p>
<ul>
    <li><a href="#help-sfguard-permission-add">Adicionar permissão</a></li>
    <li><a href="#help-sfguard-permission-edit">Editar permissão</a></li>
    <li><a href="#help-sfguard-permission-remove">Remover permissão</a></li>
</ul>
<br/>
<p><b>Nota: </b>Ver informação sobre como preencher os detalhes da permissão <a href="#help-sfguard-permission-details">aqui</a>.</p>
<hr/>

<a id="help-sfguard-permission-add"><h2>Adicionar Permissão:</h2></a>
<p>Para adicionar uma permissão, seleccionar a opção <em>Adicionar Permissão</em>.</p>
<p>Sob o lado direito (<a href="#help-sfguard-permission-details"><em>Detalhes da permissão</em></a>) devem ser preenchidos os campos que caracterizam a permissão,
    sendo os seguintes de <b>carácter obrigatório</b>: <em>Nome</em>; <em>Descrição</em>.</p>
<a href="#help-sfguard-permission-main"><div>Início</div></a>
<hr/>

<a id="help-sfguard-permission-edit"><h2>Editar Permissão:</h2></a>
<p>Para editar uma permissão, seleccionar a linha correspondente e de seguida escolher a opção <em>Editar permissão</em>,
 ou efectuar duplo clique sobre a linha.
</p>
<p>As informações existentes sobre a permissão são carregadas para o painel da direita (<a href="#help-sfguard-permission-details"><em>Detalhes da permissão</em></a>),
    onde é possível proceder às alterações necessárias.</p>

<a href="#help-sfguard-permission-main"><div>Início</div></a>
<hr/>

<a id="help-sfguard-permission-remove"><h2>Remover Permissão:</h2></a>
<p>Para remover uma permissão, seleccionar a linha correspondente e de seguida escolher a opção <em>Remover permissão</em>.</p>
<p>De seguida responder afirmativamente à mensagem de confirmação.</p>
<a href="#help-sfguard-permission-main"><div>Início</div></a>
<hr/>


<a id="help-sfguard-permission-details"><h2>Detalhes da permissão:</h2></a>
<p>É possível definir os atributos que definem a permissão. Para o efeito, deve usar-se o painel <em>Detalhes da permissão</em> que se encontra no lado direito da janela.</p>
<p>Eis uma breve descrição dos atributos existentes:</p>
<ul>
    <li><b>Nome:</b> Nome da permissão (que serve para referência na definição de grupos de permissões, ou atribuição de permissões a utilizadores);</li>
    <li><b>Descrição:</b> Sumário da permissão;</li>
    <li><b>Grupos:</b> Grupos aos quais a permissão é associada.</li>
</ul>

<a href="#help-sfguard-permission-main"><div>Início</div></a>
