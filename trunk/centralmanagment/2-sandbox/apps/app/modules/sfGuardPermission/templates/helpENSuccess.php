<a id="help-sfguard-permission-main"><h1>Manage Permissions</h1></a>
<p>
This tab have the options for managing permissions. The list (below on left) indicates the permissions on the system. The lines can be selected from the list whenever you want to make changes.
</p>
<p>The following operations are available:</p>
<ul>
    <li><a href="#help-sfguard-permission-add">Add permission</a></li>
    <li><a href="#help-sfguard-permission-edit">Edit permission</a></li>
    <li><a href="#help-sfguard-permission-remove">Remove permission</a></li>
</ul>
<br/>
<p><b>Note: </b>View information on how to fill in the permission's details <a href="#help-sfguard-permission-details">here</a>.</p>
<hr/>

<a id="help-sfguard-permission-add"><h2>Add permission:</h2></a>
<p>
To add a permission, select the <em>Add Permission</em> button.
</p>
<p>
On the right side (<a href="#help-sfguard-permission-details"><em>Permission details</em></a>) you must fill the fields that characterize the permission. The following mandatory: <em>Name</em>; <em> Description </em>.
</p>
<a href="#help-sfguard-permission-main"><div>Index</div></a>
<hr/>

<a id="help-sfguard-permission-edit"><h2>Edit permission:</h2></a>
<p>
To edit a permission, select the corresponding row and then press the <em>Edit permission</em> button, or make double click on the line.
</p>
<p>
The information on the existing permission are uploaded to the panel on the right (<a href="#help-sfguard-permission-details"><em>Permission details</em></a>), where you can make the necessary changes.
</p>

<a href="#help-sfguard-permission-main"><div>Index</div></a>
<hr/>

<a id="help-sfguard-permission-remove"><h2>Remove permission:</h2></a>
<p>
To remove a permission, select the corresponding row and then choose the <em>Remove Permission</em> option.
</p>
<p>
Then answer yes to the confirmation message.
</p>
<a href="#help-sfguard-permission-main"><div>Index</div></a>
<hr/>

<a id="help-sfguard-permission-details"><h2>Permission's details:</h2></a>
<p>
You can set the attributes that define the permission. To this end, you should use the <em>Permission detail's</em> panel.
</p>
<p>Here is a brief description of the existing attributes:</p>
<ul>
    <li><b>Name:</b> Permission's name (which is used to reference the definition of groups of permissions, or to assign permissions to users);
    <li><b>Description:</b> Permission's description;</li>
    <li><b>Groups:</b> Associated groups.</li>
</ul>

