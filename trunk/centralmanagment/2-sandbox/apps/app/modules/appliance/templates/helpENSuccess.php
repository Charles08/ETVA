<a id="help-appliance-main"><h1>Appliance</h1></a>
<p>
The appliance options are available on the <em>Tools</em> menu, and contains the following maintenance operations:
</p>
<ul>
    <li><a href="#help-appliance-register">Register</a></li>
    <li><a href="#help-appliance-backup">Backup</a></li>
    <li><a href="#help-appliance-restore">Restore</a></li>
</ul>
<br/>
<hr/>


<a id="help-appliance-register"><h2>Register Appliance</h2></a>
<p>The registration of ETVA Appliance consists in assigning a <em>SERIAL NUMBER</em>. This number is given by the registration server.</p>
<p>To start the process, the user must enter their credentials to access the server, and optionally enter a description about the appliance.</p>
<p>If registration succeeds, it becomes possible to perform the <a href="#help-appliance-backup">Backup</a> and <a href="#help-appliance-backup">Restore</a> operations.</p>
<a href="#help-appliance-main"><div>Index</div></a>
<hr/>

<a id="help-appliance-backup"><h2>Backup</h2></a>
<p>To backup the appliance, press the button <em>MAKE BACKUP!</em>.</p>
<p><b>Note 1: </b>The appliance must be registered - see <a href="#help-appliance-register">here</a>.</p>
<p><b>Note 2: </b>The virtualization agent must be running.</p>
<p><b>Note 3: </b>The appliance must have internet access.</p>
<a href="#help-appliance-main"><div>Index</div></a>
<hr/>

<a id="help-appliance-restore"><h2>Restore</h2></a>
<p>
In appliance restore we can select from a backup list which data must be recovered. Then, the next panel, show us some details about the backup. The restore process replaces the virtualization agent settings.
<p>
The appliance restore consists of the following steps:
</p>
<ol>
    <li>Select a backup;</li>
    <li>Right click on the mouse to access the context menu;</li>
    <li>Press the <em>Restore backup</em> button;</li>
    <li>Answer yes to the confirmation question.</li>
</ol>

<p><b>Note 1: </b>The appliance must be registered - see <a href="#help-appliance-register">here</a>.</p>
<p><b>Note 2: </b>The virtualization agent must be running.</p>
<p><b>Note 3: </b>The appliance must have internet access.</p>
<p><b>Note 4: </b>In the appliance restore may be necessary to manually associate the storage units.</p>
