<a id="help-appliance-main"><h1>Appliance</h1></a>
<p>O menu appliance está disponível no menu de ferramentas, e contém as seguintes operações de manutenção:</p>
<ul>
    <li><a href="#help-appliance-register">Registo</a></li>
    <li><a href="#help-appliance-backup">Backup</a></li>
    <li><a href="#help-appliance-restore">Restore</a></li>
</ul>
<br/>
<hr/>


<a id="help-appliance-register"><h2>Registo da Appliance</h2></a>
<p>O registo da Appliance ETVA consiste na atribuição de um SERIAL NUMBER por parte do servidor de registo ao <em>Central Management</em>.</p>
<p>Para dar início ao processo o utilizador deve introduzir as suas credenciais de acesso ao servidor, e opcionalmente inserir uma descrição sobre a appliance.</p>
<p>Caso o registo seja bem sucedido, passa a ser possível efectuar as operações de <a href="#help-appliance-backup"><em>Backup</em></a> e <a href="#help-appliance-backup">Restore</a>.</p>
<a href="#help-appliance-main"><div>Início</div></a>
<hr/>

<a id="help-appliance-backup"><h2>Backup da Appliance</h2></a>
<p>Para efecutar o <em>backup</em> da <em>appliance</em> seleccionar na janela o botão <em>EFECTUAR BACKUP!</em>.</p>
<p><b>Nota 1: </b>É necessário efectuar previamente o <a href="#help-appliance-register">Registo</a> da appliance.</p>
<p><b>Nota 2: </b>O agente de virtualização do <em>Node</em> necessita de estar activo.</p>
<p><b>Nota 3: </b>A <em>appliance</em> deve ter conectividade com a internet.</p>
<a href="#help-appliance-main"><div>Início</div></a>
<hr/>

<a id="help-appliance-restore"><h2>Restore da Appliance</h2></a>
<p>A janela de restauro da <em>Appliance</em> é constituída por um painel com lista de <em>backups</em> realizados,
    seguido por um painel que mostra a informação relativa ao <em>backup</em> seleccionado. O restauro repõe as configurações no agente de virtualização.</p>

<p>O processo de restauro da appliance consiste nas seguintes etapas: </p>
<ol>
    <li>Seleccionar um <em>backup</em>;</li>
    <li>Com o botão direito do rato aceder ao menu de contexto;</li>
    <li>Escolher a opção <em>Restaurar backup</em>;</li>
    <li>Responder afirmativamente na janela de confirmação.</li>
</ol>

<p><b>Nota 1: </b>Para proceder à operação de restauro é necessário efectuar previamente o <a href="#help-appliance-register">Registo da</a> appliance.</p>
<p><b>Nota 2: </b>O agente de virtualização do <em>Node</em> necessita de estar activo.</p>
<p><b>Nota 3: </b>A <em>appliance</em> deve ter conectividade com a internet.</p>
<p><b>Nota 4: </b>No processo de restauro poderá ser necessário associar manualmente as unidades de armazenamento.</p>
