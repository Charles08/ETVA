    <a id="help-event-diagnose"><h1>Janela de diagnóstico</h1></a>
    
    <p>Para aceder à janela de diagnóstico utiliza-se o botão <em>Diagnosticar</em>.</p>
    <p>A janela de diagnóstico possibilita a recolha de todas as informações necessárias à identificação de problemas no sistema.</p>
    <p>O processo de recolha segue a seguinte cronologia:</p>
    <ul>
        <li><b>Gerar ficheiro:</b> agrega a informação sobre o central management e os agentes de virtualização em execução;</li>
        <li><b>Descarregar:</b> disponível após a criação do ficheiro;</li>
        <li><b>Enviar:</b> envia o ficheiro com a informação para o endereço de email especificado.</li>
    </ul>
