<a id="help-physical-vol"><h1>Devices</h1></a>
<p>Informação relativa aos <em>physical volumes</em> e seu estado (Um <em>physical volume</em> é um dispositivo físico, como por exemplo um disco). 
    Nesta grelha é possível fazer a administração de <em>physical volumes</em>, nomeadamente as seguintes operações:</p>

<ul>
    <li><a href="#help-physical-vol-start">Iniciar <em>physical volume</em>;</a></li>
    <li><a href="#help-physical-vol-stop">Parar <em>physical volume</em>.</a></li>
    <li><a href="#help-physical-vol-scan">Procurar <em>physical volumes</em>.</a></li>
</ul>

<br/>
<hr/>

<a id="help-physical-vol-start"><h1>Iniciar <em><b>Physical Volume</b></em></h1></a>
<p>Para <b>inicializar</b> um <em>physical volume</em> aceder ao sub-menu de contexto do dispositivo pretendido,
    e seleccionar <em>Inicializar physical volume</em>.</p>

<a href="#help-physical-vol"><div>Início</div></a>
<hr/>


<a id="help-physical-vol-stop"><h1>Parar <em><b>Physical Volume</b></em></h1></a>
<p>Para <b>remover</b> um <em>physical volume</em> a operação é análoga,
    bastando seleccionar a opção <em>Remover inicialização do physical volume</em> no sub-menu de contexto do <em>physical volume</em>.
</p>
<p><b>Nota </b>Só é permitido remover um <em>physical volume</em> se este não pertencer a nenhum <em>volume group</em>.</p>
<a href="#help-physical-vol"><div>Início</div></a>
<hr/>

<a id="help-physical-vol-scan"><h1>Procurar <em><b>Physical Volumes</b></em></h1></a>
<p>Em <em>Procurar physical devices</em> é possível correr uma tarefa do lado do agente de virtualização que procura discos no sistema e possibilita o registo no <em>Central Management</em>. Analogamente, é possível remover o registo de um physical device do <em>Central Management</em>, caso se pretenda que este deixe ser gerido pelo sistema.</p>
<a href="#help-physical-vol"><div>Início</div></a>
