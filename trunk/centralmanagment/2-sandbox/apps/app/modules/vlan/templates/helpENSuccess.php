<a id="help-vlan"><h1>Manage Network</h1></a>
<p>This panel allows you to perform the following operations on the CM:
</p>

<ul>
    <li><a href="#help-vlan-manage-add">Add network</a></li>
    <li><a href="#help-vlan-manage-remove">Remove network</a></li>
    <li><a href="#help-vlan-mac">MAC Pool Management</a></li>
    <li><a href="#help-vlan-list">List of network interfaces</a></li>
</ul>
<p><b>Note: </b>Some operations are only available in <em>Enterprise</em> version.</p>
<br/>
<hr/>

<a id="help-vlan-manage-add"><h1>Add networks</h1></a>
<p>To create a network in the selected cluster/datacenter, click on <em>Add network</em> button. The network information consists of his name and ID (If the network/vlan is tagged the ID field of the network refers to the VLAN ID).</p>

<p><b>Note 1: </b>
The operation to add the network is only available in <em>Enterprise</em> version.
</p>

<p><b>Note 2: </b>
The added network is propagated to all nodes of the selected cluster.
</p>
<a href="#help-vlan"><div>Index</div></a>
<hr/>

<a id="help-vlan-manage-remove"><h1>Remove Networks</h1></a>
<p>
To remove a network, you choose the desired network and click on <em>Remove network</em> button.
</p>

<p><b>Note 1: </b>This option is only available in <em>Enterprise</em> version.</p>
<p><b>Note 2: </b>The operation is propagated to all nodes of the cluster.</p>
<a href="#help-vlan"><div>Index</div></a>
<hr/>

<a id="help-vlan-mac"><h1>MAC address pool management</h1></a>
<p>In <em>MAC Pool Management</em> you can create a pool of MAC addresses. We can also see the associated networks and available MACs.
</p>
<a href="#help-vlan"><div>Index</div></a>
<hr/>

<a id="help-vlan-list"><h1>List of network interfaces</h1></a>
<p>It is possible to list the network interfaces for a given network. To do this, select the desired network.
They will appear in the panel below the network interfaces that are associated.
</p>
<a href="#help-vlan"><div>Index</div></a>
