<a id="help-vol-group"><h1>Volume Groups</h1></a>
<p>Na administração de <em>volumes groups</em> é permitido:</p>

<ul>
    <li><a href="#help-vol-group-add">Criar</a></li>
    <li><a href="#help-vol-group-extend">Estender</a></li>
    <li><a href="#help-vol-group-rm_rs">Reduzir</a></li>
    <li><a href="#help-vol-group-rm_rs">Remover</a></li>
</ul>

<br/>
<hr/>


<a id="help-vol-group-add"><h1>Criar <em><b>Volume Group</b></em></h1></a>
<p>Para criar um <em>volume group</em> acede-se ao sub-menu de contexto sobre um qualquer <em>volume
group</em> e seleccionar <em>Adicionar volume group</em>. Na janela de criação deverá ser introduzido o
nome pretendido e seleccionar um ou mais <em>physical voumes</em> disponíveis.
</p>
<p>
Um <em>physical volume</em> está disponível quando não está alocado a nenhum <em>volume group</em> e
encontra-se inicializado.
</p>
<a href="#help-vol-group"><div>Início</div></a>
<hr/>

<a id="help-vol-group-extend"><h1>Estender <em><b>Volume Group</b></em></h1></a>
<p>Para extender um <em>volume group</em> recorre-se ao <em>drag-n-drop</em>, ou seja, arrasta-se o <em>physical
volume</em>, que se pretende adicionar, para cima do <em>volume group</em> pretendido.
</p>
<a href="#help-vol-group"><div>Início</div></a>
<hr/>


<a id="help-vol-group-rm_rs"><h1>Remover / Reduzir <em><b>Volume Group</b></em></h1></a>
<p>Na remoção/redução de um <em>volume group</em> seleccciona-se o <em>volume group/physical</em> volume
a remover e escolhe-se a opção correspondente do sub-menu de contexto.
</p>
<p><b>Nota </b>Só é permitido remover um <em>volume group</em> se não houver nenhum <em>logical volume</em>
associado.</p>
<a href="#help-vol-group"><div>Início</div></a>
