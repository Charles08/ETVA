<a id="help-vol-group-add"><h1>Add <em><b>Volume Group</b></em></h1></a>
<p>
To create a volume group, access to the sub-context menu on any volume group and select <em>Add volume group</em>. In the creation window you must introduce the desired name and select one or more available physical volumes.
</p>
<p>
A physical volume is available when it is not allocated to any volume group and is initialized.
</p>
<a href="#help-vol-group"><div>Index</div></a>
<hr/>

<a id="help-vol-group-extend"><h1>Extend <em><b>Volume Group</b></em></h1></a>
<p>To extend a volume group use the drag-n-drop, drag the physical volume that you want to add up the volume group you want.</p>
<a href="#help-vol-group"><div>Index</div></a>
<hr/>

<a id="help-vol-group-rm_rs"><h1>Remove/Re-size <em><b>Volume Group</b></em></h1></a>
To remove/shrink a volume group, select the desired logical/physical volume and choose the option remove (available on context menu).
<p><b>Note</b>We can remove a volume group if there is no logical volume associated with it.
