<a id="help-sfguard-user-main"><h1>User and permission administration</h1></a>
<p>
In this tab we can find the options for managing users. The list (below on left) indicates users in the system. The lines can be selected from the list, where you want to make changes to a user.</p>
<p>The following operations are available:</p>
<ul>
    <li><a href="#help-sfguard-user-add">Add user</a></li>
    <li><a href="#help-sfguard-user-edit">Edit user</a></li>
    <li><a href="#help-sfguard-user-remove">Remove user</a></li>
</ul>
<br/>
<p><b>Note: </b>To learn how to fill out your form see this <a href="#help-sfguard-user-details">link</a>.</p>
<hr/>

<a id="help-sfguard-user-add"><h2>Add user:</h2></a>
<p>
To add a user, press the <em>Add user</em> button.
</p>
<p>
At the right (<a href="#help-sfguard-user-details"><em>User details</em></a>) fill out the form. 
</p>
<a href="#help-sfguard-user-main"><div>Index</div></a>
<hr/>

<a id="help-sfguard-user-edit"><h2>Edit user:</h2></a>
<p>
To edit a user, select the corresponding row and then choose the option <em> Edit user</em>, or make double click on the line.
</p>
<p>
The existing information about the user are loaded into the right pane (<a href="#help-sfguard-user-details"><em>User details</em></a>), where you can make the necessary changes.
</p>
<a href="#help-sfguard-user-main"><div>Index</div></a>
<hr/>

<a id="help-sfguard-user-remove"><h2>Remove user:</h2></a>
<p>
To remove a user, select the corresponding row and then choose the <em>Remove user</em>.
</p>
<p>Then answer yes to the confirmation message.</p>
<a href="#help-sfguard-user-main"><div>Index</div></a>
<hr/>


<a id="help-sfguard-user-details"><h2>User details:</h2></a>
<p>
You can set the attributes that define the user. This panel should be user for this purpose.
</p>
<p>Here is a brief description of the existing attributes:</p>
<ul>
    <li><b>Username:</b>Name the user uses to log into central management;</li>
    <li><b>First name:</b> User's first name;</li>
    <li><b>Last name:</b> User's last name;</li>
    <li><b>Password:</b> User's password;</li>
    <li><b>Confirm New Password:</b> Password again;</li>
    <li><b>Email:</b> Email address;</li>
    <li><b>Active:</b> Indicates whether the user may or may not log in central management;</li>
    <li><b>Super Admin</b>Assign all the privileges of to the user;</li>
    <li><b>Groups:</b> Groups the user belongs. Note that each group are associated with permissions that allow the user to access certain services;</li>
    <li><b>Permissions</b>Permissions assigned to the user.</li>
</ul>

