<a id="help-sfguard-user-main"><h1>Gestão de utilizadores</h1></a>
<p>Neste separador encontram-se as opções de gestão de utilizadores. A listagem (em baixo sobre a esquerda) indica os
utilizadores existentes no sistema. As linhas da listagem podem ser selecionadas, sempre que se pretender efectuar alterações
num utilizador.</p>
<p>Estão disponíveis as seguintes operações:</p>
<ul>
    <li><a href="#help-sfguard-user-add">Adicionar Utilizador</a></li>
    <li><a href="#help-sfguard-user-edit">Editar Utilizador</a></li>
    <li><a href="#help-sfguard-user-remove">Remover Utilizador</a></li>
</ul>
<br/>
<p><b>Nota: </b>Ver informação sobre como preencher os detalhes dos utilizadores <a href="#help-sfguard-user-details">aqui</a>.</p>
<hr/>

<a id="help-sfguard-user-add"><h2>Adicionar Utilizador:</h2></a>
<p>Para adicionar utilizadores, seleccionar a opção <em>Adicionar Utilizador</em>.</p>
<p>Sob o lado direito (<a href="#help-sfguard-user-details"><em>Detalhes de utilizador</em></a>) devem ser preenchidos os campos que caracterizam o utilizador,
    sendo os seguintes de <b>carácter obrigatório</b>: <em>Username</em>; <em>Primeiro nome</em>;
 <em>Password</em>; <em>Confirmar Password</em>.</p>
<a href="#help-sfguard-user-main"><div>Início</div></a>
<hr/>

<a id="help-sfguard-user-edit"><h2>Editar Utilizador:</h2></a>
<p>Para editar um utilizador, seleccionar a linha correspondente e de seguida escolher a opção <em>Editar utilizador</em>,
 ou efectuar duplo clique sobre a linha.
</p>
<p>As informações existentes sobre o utilizador são carregadas para o painel da direita (<a href="#help-sfguard-user-details"><em>Detalhes de utilizador</em></a>),
    onde é possível proceder às alterações necessárias.</p>

<a href="#help-sfguard-user-main"><div>Início</div></a>
<hr/>

<a id="help-sfguard-user-remove"><h2>Remover Utilizador:</h2></a>
<p>Para remover um utilizador, seleccionar a linha correspondente e de seguida escolher a opção <em>Remover utilizador</em>.</p>
<p>De seguida responder afirmativamente à mensagem de confirmação.</p>
<a href="#help-sfguard-user-main"><div>Início</div></a>
<hr/>


<a id="help-sfguard-user-details"><h2>Detalhes de utilizador:</h2></a>
<p>É possível definir os atributos que definem o utilizador. Para o efeito, deve usar-se o painel <em>Detalhes de utilizador</em> que se encontra no lado direito da janela.</p>
<p>Eis uma breve descrição dos atributos existentes:</p>
<ul>
    <li><b>Username:</b> Nome que o utilizador usa para se autienticar no <em>Central Management</em>;</li>
    <li><b>Primeiro nome:</b> Primeiro nome do utilizador;</li>
    <li><b>Último nome:</b> Último nome do utilizador;</li>
    <li><b>Password:</b> Palavra-chave a usar pelo utilizador no processo de autenticação;</li>
    <li><b>Confirmar Password:</b> Repetição da palavra-chava (evita configurar incorrectamente a <em>password</em> do utilizador);</li>
    <li><b>Email:</b> Endereço de correio electrónico;</li>
    <li><b>Activo:</b> Indica se o utilizador pode, ou não, autenticar-se no <em>Central Management</em>;</li>
    <li><b>Super Admin</b> Atribui ao utilizador todos os privilégios de administração;</li>
    <li><b>Grupos:</b> Grupos aos quais o utilizador pertence. Note que a cada grupo estão associadas permissões, que permitem ao utilizador aceder a certos serviços;</li>
    <li><b>Permissões</b> Permissões isoladas atribuidas ao utilizador.</li>
</ul>

