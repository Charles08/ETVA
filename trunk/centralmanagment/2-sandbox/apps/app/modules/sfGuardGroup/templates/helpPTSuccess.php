<a id="help-sfguard-group-main"><h1>Gestão de grupos de permissões</h1></a>
<p>Neste separador encontram-se as opções de gestão de grupos. A listagem (em baixo sobre a esquerda) indica os
grupos existentes no sistema. As linhas da listagem podem ser selecionadas sempre que se pretender efectuar alterações.</p>
<p>Estão disponíveis as seguintes operações:</p>
<ul>
    <li><a href="#help-sfguard-group-add">Adicionar Grupo</a></li>
    <li><a href="#help-sfguard-group-edit">Editar Grupo</a></li>
    <li><a href="#help-sfguard-group-remove">Remover Grupo</a></li>
</ul>
<br/>
<p><b>Nota: </b>Ver informação sobre como preencher os detalhes do grupo <a href="#help-sfguard-group-details">aqui</a>.</p>
<hr/>

<a id="help-sfguard-group-add"><h2>Adicionar Grupo:</h2></a>
<p>Para adicionar um grupo, seleccionar a opção <em>Adicionar grupo</em>.</p>
<p>Sob o lado direito (<a href="#help-sfguard-group-details"><em>Detalhes da Grupo</em></a>) devem ser preenchidos os campos que caracterizam o grupo,
    sendo os seguintes de <b>carácter obrigatório</b>: <em>Nome</em>; <em>Descrição</em>.
<a href="#help-sfguard-group-main"><div>Início</div></a>
<hr/>

<a id="help-sfguard-group-edit"><h2>Editar Grupo:</h2></a>
<p>Para editar um grupo, seleccionar a linha correspondente e de seguida escolher a opção <em>Editar grupo</em>,
 ou efectuar duplo clique sobre a linha.
</p>
<p>As informações existentes sobre o grupo são carregadas para o painel da direita (<a href="#help-sfguard-group-details"><em>Detalhes do Grupo</em></a>),
    onde é possível proceder às alterações necessárias.</p>

<a href="#help-sfguard-group-main"><div>Início</div></a>
<hr/>

<a id="help-sfguard-group-remove"><h2>Remover Grupo:</h2></a>
<p>Para remover um grupo, seleccionar a linha correspondente e de seguida escolher a opção <em>Remover grupo</em>.</p>
<p>De seguida responder afirmativamente à mensagem de confirmação.</p>
<a href="#help-sfguard-group-main"><div>Início</div></a>
<hr/>


<a id="help-sfguard-group-details"><h2>Detalhes do Grupo:</h2></a>
<p>É possível definir os atributos que definem cada grupo. Para o efeito, deve usar-se o painel <em>Detalhes da Grupo</em> que se encontra no lado direito da janela.</p>
<p>Eis uma breve descrição dos atributos existentes:</p>
<ul>
    <li><b>Nome:</b> Nome do grupo (que serve para referência na de grupos a permissões, ou atribuição de grupos a utilizadores);</li>
    <li><b>Descrição:</b> Sumário do grupo;</li>
    <li><b>Permissões:</b> Permissões associadas ao grupo.</li>
</ul>

<a href="#help-sfguard-group-main"><div>Início</div></a>
