<a id="help-sfguard-group-main"><h1>Manage Groups</h1></a>
<p>
This tab have the options for managing groups. The list (below on left) indicates the existing groups. The lines can be selected from the list whenever you want to make changes.
</p>
<p>The following operations are available:</p>
<ul>
    <li><a href="#help-sfguard-group-add">Add group</a></li>
    <li><a href="#help-sfguard-group-edit">Edit group</a></li>
    <li><a href="#help-sfguard-group-remove">Remove group</a></li>
</ul>
<br/>
<p><b>Note: </b>View information on how to fill in the details of the group <a href="#help-sfguard-group-details">here</a>.</p>
<hr/>

<a id="help-sfguard-group-add"><h2>Add group:</h2></a>
<p>
To add a group, select the <em>Add group</em> option.
</p>
<p>
On the right side (<a href="#help-sfguard-group-details"><em>Group details</em></a>) we find the fields that characterize the group. Some of those are mandatory: <em>Name</em>; <em>Description</em>.
</p>
<a href="#help-sfguard-group-main"><div>Index</div></a>
<hr/>

<a id="help-sfguard-group-edit"><h2>Edit group:</h2></a>
<p>
To edit a group, select the corresponding row and then choose the <em>Edit group</em> option or make double click on the line.
</p>
<p>
The existing informations about the group are loaded into the right pane (<a href="#help-sfguard-group-details"><em>Group details</em></a>), where you can make the necessary changes.
</p>

<a href="#help-sfguard-group-main"><div>Index</div></a>
<hr/>

<a id="help-sfguard-group-remove"><h2>Remove group:</h2></a>
<p>
To remove a group, select the corresponding row and then choose the <em>Remove group</em> option.
</p>
<p>
Then answer yes to the confirmation message.
</p>
<a href="#help-sfguard-group-main"><div>Index</div></a>
<hr/>


<a id="help-sfguard-group-details"><h2>Group details:</h2></a>
<p>
You can set the attributes that define each group. To this end, you should use the <em>Group details</em> pane which is on the right side.
</p>
<p>Here is a brief description of the existing attributes:</p>
<ul>
    <li><b>Name:</b>Group name (used to reference the group permissions, or for assigning users to groups);</li>
    <li><b>Description:</b> Group's description;</li>
    <li><b>Permissions:</b> Permissions associated with the group.</li>
</ul>

